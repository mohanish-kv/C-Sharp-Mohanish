using System;

namespace MathLib
{
    public class Calculator
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }

        public int Product(int a, int b)
        {
            return a * b;
        }

        public int Difference(int a, int b)
        {
            return a - b;
        }

        public double Divide(double a, double b)
        {
            return a / b;
        }
    }
}